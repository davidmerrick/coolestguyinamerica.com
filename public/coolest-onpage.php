<h2>Optimize On-Page</h2>
<p>Optimizing On-Page means making the actual content of your site easier to read by search engines. <br /><br />
The way that search engines work is that they have programs called Spiders that "crawl" web pages and 
add them to the search engine's index. The search engine then looks at your content on the page in order to decide what 
keywords your site is relevant for and compares it to other sites.</p>
<p>A few years ago a group of SEOs (Search Engine Optimizers) set up a network of links to the White House's website
so that whenever someone would search "miserable failure", the White House's page would be the top result. Search Engine
tricks like these are known in the industry as "Google Bombs"
</p>
<p>
Google has since then refined their algorithm (the formula that it uses to rank sites) so that a site must have the words 
on the page in order to rank for them.
</p>
<p>
What's important now is the following:
<ul>
	<li>Use your keywords <b>early and often</b>--but not too often. Aim for 3 percent keyword density.</li>
	<li>Use your keywords in your meta description and meta title tags</li>
	<li>Use your keywords in h1, h2, h3 tags</li>
	<li>Use your keywords in your image "alt" tags</li>
	<li>Use your keywords in your URL</li>
	<li>Your Meta tag description should have at least 50 characters</li>
	<li>Your keywords meta tag should have at least 4 keywords</li>
	<li>Include no more than 100 URLs on a page</li>
	<li>You should have outbound links on your page to high quality and relevant websites.</li>
	<li>Remember: Google ranks pages, not sites</li>
</ul>

</p>

